package com.st.btc.pojo;

public class Product {

	private Integer pid;
	private String pname;
	private String pdept;
	private Double pprice;
	private Integer pcount;
	private Integer psales;
	private Integer pftype;
	private Integer pstype;
	private String ppic;
	private Integer pstatus;
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPdept() {
		return pdept;
	}
	public void setPdept(String pdept) {
		this.pdept = pdept;
	}
	public Double getPprice() {
		return pprice;
	}
	public void setPprice(Double pprice) {
		this.pprice = pprice;
	}
	public Integer getPcount() {
		return pcount;
	}
	public void setPcount(Integer pcount) {
		this.pcount = pcount;
	}
	public Integer getPsales() {
		return psales;
	}
	public void setPsales(Integer psales) {
		this.psales = psales;
	}
	public Integer getPftype() {
		return pftype;
	}
	public void setPftype(Integer pftype) {
		this.pftype = pftype;
	}
	public Integer getPstype() {
		return pstype;
	}
	public void setPstype(Integer pstype) {
		this.pstype = pstype;
	}
	public String getPpic() {
		return ppic;
	}
	public void setPpic(String ppic) {
		this.ppic = ppic;
	}
	public Integer getPstatus() {
		return pstatus;
	}
	public void setPstatus(Integer pstatus) {
		this.pstatus = pstatus;
	}
	
}
